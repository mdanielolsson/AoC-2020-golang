	data := strings.Split(input, "\n")

	fmt.Printf("Part 1:\t")
	fmt.Println(path(data, 3, 1))

	fmt.Printf("Part 2:\t")
	result := multiplyAll([]int{
		path(data, 1, 1),
		path(data, 3, 1),
		path(data, 5, 1),
		path(data, 7, 1),
		path(data, 1, 2),
	})
	fmt.Println(result)
