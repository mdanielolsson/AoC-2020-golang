func path(data []string, right, down int) int {
	pos := right
	countTrees := 0
	for i := down; i < len(data); i = i + down {
		lastPos := len(data[i]) - 1
		if string(data[i][pos]) == `#` {
			countTrees++
		}
		pos = pos + right
		if lastPos < pos {
			pos = pos - lastPos - 1
		}
	}
	return countTrees
}

// multiplyAll will multiply all values of a slice of integers and return the result
func multiplyAll(items []int) int {
	result := 1
	for _, v := range items {
		result = result * v
	}
	return result
}

